# Qué es el análisis de datos?
# Es el proceso de recopilar, organizar e interpretar datos
# Por qué es importante?
# Para tomar decisiones informadas, identificar patrones y resolver problemas
# Cual es el ciclo de vida del análisis de datos
#   Recopilación (Reúne los datos)
#   Limpieza (Asegúrate de que sean confiables)
#   Análisis (Extrae información valiosa)
#   Visualización (Comunica los hallazgos claramete)

# Recopilación de datos
#   Es el proceso de reunir información relevante de diversas fuentes
#   Algunos ejemplos son los archivos CSV, Bases de datos, APIs y encuentas

# Limpieza de datos
#   Es el proceso de corregir, eliminar o modificar datos incompletos, duplicados o incorrectos
#   Algunos ejemplos es limpiar valores nulos o duplicados, estandarizar formatos (fechas, numeros, etc)

# Análisis de datos
#   Es el proceso en el que se usan técnicas estadísticas o matemáticas para extraer info
#   Se pueden usar analisis descriptivos para resumir datos, o predictivos para preveer tendencias futuras

# Visualización de datos
#   Es el proceso de representar información de manera gráfica para facilitar su interpretación
#   Herramientas comunes para esto es Excel, Python y Power BI

