# Tipos y formato de datos
# En el análisis de datos, los tipos de datos determinana las técnicas
# y herramientas que se utilizarán para su procesamiento y análisis.
# Existen 3 tipos de datos y son los sigueintes:

# Datos Estructurados: 
#   Son aquellos organizados en un formato tabular, es decir en tablas con filas y columnas
#   Este tipo de datos es facil de almacenar y procesar utilizando bases de datos relacionales
#   Ejemplo de estos tenemos: registros bancarios, inventario de productos y dato de sensores

# Datos no Estructurados:
#   Carecen de un esquema predefinido y no se organizan en filas o columnas
#   Representan aproximadamente el 80% de los datos generados en el mundo
#   Pueden ser texto, imagenes, videos, audios o documentos
#   Ejemplo de esto tenemos: Publicaciones en redes sociale, imagenes de resonancia
#                            y Logs de eventos generados por aplicaciones

# Datos Semi-Estructurados:
#   Tienen un formato definido, pero no siguen un esquema fijo como tablas
#   Estan organizados en jerarquías o etiquetas
#   Utilizan formatos como: JSON, XML o YAML para almacenar datos
#   Más flexibles que los datos estructurales
